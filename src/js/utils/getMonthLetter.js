function getMonthLetter(monthNumber) {
  switch (Number(monthNumber)) {
    case Number('01'):
      return 'A';
    case Number('02'):
      return 'B';
    case Number('03'):
      return 'C';
    case Number('04'):
      return 'D';
    case Number('05'):
      return 'E';
    case Number('06'):
      return 'F';
    case Number('07'):
      return 'G';
    case Number('08'):
      return 'H';
    case Number('09'):
      return 'I';
    case Number('10'):
      return 'J';
    case Number('11'):
      return 'K';
    case Number('12'):
      return 'L';
  }
}
